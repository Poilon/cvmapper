#   Character.create(name: 'Luke', movie: movies.first)


def add_all(klass, records)
  records.each do |elements|
    trueelement = elements.first
    aliases     = elements[1..-1]

    record = klass.find_by(name: trueelement) || klass.create(name: trueelement)

    aliases.each do |aliase|
      next if record.children.pluck(:name).include?(aliase)
      klass.create(name: aliase, parent_id: record.id)
    end
  end
end

